package modes

import (
	"github.com/jalaj711/gocrypto/aes"
	"reflect"
	"testing"
)

func TestCFB_Encrypt(t *testing.T) {
	type fields struct {
		cipher  BlockCipher
		padding Padding
		iv      []byte
	}
	type args struct {
		data []byte
	}
	tests := []struct {
		name   string
		fields fields
		args   args
		want   []byte
	}{
		{
			name: "cfb-aes-enc-1",
			fields: fields{
				cipher:  &aes.AES{},
				padding: &PKCS7{},
				iv: []byte{
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
				},
			},
			args: args{
				data: []byte{
					0x00, 0x7e, 0x65, 0x77,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99,
				},
			},
			want: []byte{
				0x66, 0x27, 0xc1, 0x9c,
				0xd9, 0xf6, 0xe3, 0x50,
				0x4f, 0x6d, 0xe3, 0x6a,
				0xc4, 0x50, 0x58, 0xa9,
			},
		},
		{
			name: "cfb-aes-enc-2-multi-block",
			fields: fields{
				cipher:  &aes.AES{},
				padding: &PKCS7{},
				iv: []byte{
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
				},
			},
			args: args{
				data: []byte{
					0x00, 0x7e, 0x65, 0x77,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99, 0x00,
					0x7e, 0x65, 0x77, 0xe2,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99,
				},
			},
			want: []byte{
				0x66, 0x27, 0xc1, 0x9c,
				0xd9, 0xf6, 0xe3, 0x50,
				0x4f, 0x6d, 0xe3, 0x6a,
				0xc4, 0x50, 0x58, 0xa8,
				0x8e, 0x19, 0x45, 0x95,
				0x5b, 0x5e, 0x15, 0x2c,
				0xa0, 0xbe, 0x4f, 0x19,
				0x3b, 0x28, 0xad, 0x40,
			},
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			cfb := &CFB{
				cipher:  tt.fields.cipher,
				padding: tt.fields.padding,
				iv:      tt.fields.iv,
			}
			cfb.Init([]byte{
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
			}, []byte{})
			if got := cfb.Encrypt(tt.args.data); !reflect.DeepEqual(got, tt.want) {
				t.Errorf("Encrypt() = %x, want %x", got, tt.want)
			}
		})
	}
}

