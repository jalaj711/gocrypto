package modes

import (
	"github.com/jalaj711/gocrypto/aes"
	"reflect"
	"testing"
)

func TestCBC_Encrypt(t *testing.T) {
	type fields struct {
		cipher  BlockCipher
		padding Padding
		iv      []byte
	}
	type args struct {
		data []byte
	}
	tests := []struct {
		name   string
		fields fields
		args   args
		want   []byte
	}{
		{
			name: "cbc-aes-enc-1",
			fields: fields{
				cipher:  &aes.AES{},
				padding: &PKCS7{},
				iv: []byte{
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
				},
			},
			args: args{
				data: []byte{
					0x00, 0x7e, 0x65, 0x77,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99,
				},
			},
			want: []byte{
				0xff, 0x46, 0x30, 0xf2,
				0x5b, 0xbe, 0xb6, 0x3c,
				0x7f, 0xcf, 0x45, 0x76,
				0xf5, 0x0d, 0x4e, 0xa9,
			},
		},
		{
			name: "cbc-aes-enc-2-multi-block",
			fields: fields{
				cipher:  &aes.AES{},
				padding: &PKCS7{},
				iv: []byte{
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
					0x0, 0x0, 0x0, 0x0,
				},
			},
			args: args{
				data: []byte{
					0x00, 0x7e, 0x65, 0x77,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99, 0x00,
					0x7e, 0x65, 0x77, 0xe2,
					0xe6, 0x57, 0x7a, 0xbc,
					0x62, 0xcd, 0x28, 0x73,
					0x65, 0xf1, 0x99,
				},
			},
			want: []byte{
				0x12, 0x30, 0x78, 0xe4,
				0xfa, 0x4b, 0x01, 0x8c,
				0x6c, 0xc3, 0x2d, 0x59,
				0xf5, 0xff, 0xc6, 0xf0,
				0x2d, 0x96, 0x76, 0x85,
				0x5a, 0x64, 0x11, 0x71,
				0xa0, 0x0b, 0x9d, 0xd3,
				0x0c, 0x39, 0xbc, 0xe1,
			},
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			cbc := &CBC{
				cipher:  tt.fields.cipher,
				padding: tt.fields.padding,
				iv:      tt.fields.iv,
			}
			cbc.Init([]byte{
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00,
			}, []byte{})
			if got := cbc.Encrypt(tt.args.data); !reflect.DeepEqual(got, tt.want) {
				t.Errorf("Encrypt() = %x, want %x", got, tt.want)
			}
		})
	}
}

